-- Helper functions (bypass RLS safely)
create or replace function public.is_household_member(hid uuid)
returns boolean
language sql
security definer
set search_path = public
as $$
  select exists (
    select 1 from public.household_members hm
    where hm.household_id = hid and hm.user_id = auth.uid()
  );
$$;

create or replace function public.is_household_owner(hid uuid)
returns boolean
language sql
security definer
set search_path = public
as $$
  select exists (
    select 1 from public.households h
    where h.id = hid and h.created_by = auth.uid()
  )
  or exists (
    select 1 from public.household_members hm
    where hm.household_id = hid and hm.user_id = auth.uid() and hm.role = 'owner'
  );
$$;

create or replace function public.shares_household(target_user_id uuid)
returns boolean
language sql
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.household_members hm_self
    join public.household_members hm_other
      on hm_self.household_id = hm_other.household_id
    where hm_self.user_id = auth.uid()
      and hm_other.user_id = target_user_id
  );
$$;

grant execute on function public.is_household_member(uuid) to authenticated;
grant execute on function public.is_household_owner(uuid) to authenticated;
grant execute on function public.shares_household(uuid) to authenticated;

