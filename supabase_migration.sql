-- Item history trigger
create or replace function public.log_item_change()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user_id uuid;
begin
  v_user_id := coalesce(auth.uid(), NEW.user_id, OLD.user_id);

  if (TG_OP = 'INSERT') then
    insert into public.item_history (item_id, household_id, user_id, action, data)
    values (NEW.id, NEW.household_id, v_user_id, 'created', to_jsonb(NEW));
    return NEW;
  elsif (TG_OP = 'UPDATE') then
    insert into public.item_history (item_id, household_id, user_id, action, data, previous_data)
    values (NEW.id, NEW.household_id, v_user_id, 'updated', to_jsonb(NEW), to_jsonb(OLD));
    return NEW;
  elsif (TG_OP = 'DELETE') then
    insert into public.item_history (item_id, household_id, user_id, action, data)
    values (OLD.id, OLD.household_id, v_user_id, 'deleted', to_jsonb(OLD));
    return OLD;
  end if;

  return null;
end;
$$;

drop trigger if exists items_history_trigger on public.items;
create trigger items_history_trigger
after insert or update or delete on public.items
for each row execute function public.log_item_change();

-- Join household RPC (security definer)
create or replace function public.join_household(join_code text)
returns uuid
language plpgsql
security definer
set search_path = public
as $$
declare
  v_household_id uuid;
begin
  select id into v_household_id from public.households where households.join_code = join_code;
  if v_household_id is null then
    raise exception 'Invalid join code';
  end if;

  insert into public.household_members (household_id, user_id, role)
  values (v_household_id, auth.uid(), 'member')
  on conflict do nothing;

  return v_household_id;
end;
$$;

-- Create household RPC (security definer)
create or replace function public.create_household(household_name text, join_code text)
returns public.households
language plpgsql
security definer
set search_path = public
as $$
declare
  v_household public.households;
begin
  if auth.uid() is null then
    raise exception 'Not authenticated';
  end if;

  insert into public.households (name, join_code, created_by)
  values (household_name, join_code, auth.uid())
  returning * into v_household;

  insert into public.household_members (household_id, user_id, role)
  values (v_household.id, auth.uid(), 'owner')
  on conflict do nothing;

  insert into public.user_settings (user_id, active_household_id)
  values (auth.uid(), v_household.id)
  on conflict (user_id)
  do update set active_household_id = excluded.active_household_id;

  return v_household;
end;
$$;

grant execute on function public.join_household(text) to authenticated;
grant execute on function public.create_household(text, text) to authenticated;
